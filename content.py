# 2) SCRIPTED CONVERSATIONS — branching dialogues, no API
# ----------------------------------------------------------------------
# Each conversation is a small tree of NODES. The learner reads/hears what
# the character says, then chooses one of several replies (they can also
# say it out loud — speech is scored). Each reply leads to the next node.
#
# Node shape:
#   "id":   unique node id within this conversation
#   "npc":  {"en": what the character says, "ar": translation}
#   "replies": [ {"en": learner line, "ar": translation, "goto": next_node_id}, ... ]
#   A node with "end": True (and no replies) finishes the conversation.
#
# This is deterministic: same choices, same path. No AI, always works.

def _n(id, npc_en, npc_ar, replies=None, end=False):
    node = {"id": id, "npc": {"en": npc_en, "ar": npc_ar}}
    if replies:
        node["replies"] = [{"en": r[0], "ar": r[1], "goto": r[2]} for r in replies]
    if end:
        node["end"] = True
    return node


